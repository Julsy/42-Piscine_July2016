int		ft_strcmp(const char *s1, const char *s2)
{
	unsigned int i;

	i = 0;
	if (s1[0] != '\0' || s2[0])
	{
		while (s1[i] == s2[i])
		{
			if (s1[i] == '\0' && s2[i] == '\0')
				return (0);
			i++;
		}
		if (s1[i] != s2[i])
			return (int)(s1[i] - s2[i]);
	}
	return (0);
}
