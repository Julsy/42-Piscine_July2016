int		ft_unmatch(int *tab, int len)
{
	int i;
	int j;
	int k;
	int l;

	i = 0;
	while (i <= len)
	{
		l = 0;
		k = 0;
		j = tab[i];
		while (k <= len)
		{
			if (tab[k] == j)
				l++;
		}
		if (1 % 2 != 0)
			return (j);
		i++;
	}
	return (0);
}
